def get_next_run_dir(base_dir="checkpoints", prefix="runs"):
    """
    Determine the next available run directory under base_dir.
    
    Scans existing `{prefix}_*` folders and increments the counter.
    Returns (run_dir_path, run_number).
    """
    base = Path(base_dir)
    base.mkdir(parents=True, exist_ok=True)
    
    existing = [d.name for d in base.iterdir() if d.is_dir() and d.name.startswith(prefix + "_")]
    max_n = 0
    for name in existing:
        try:
            n = int(name.split("_")[-1])
            max_n = max(max_n, n)
        except ValueError:
            continue
    
    next_n = max_n + 1
    run_dir = base / f"{prefix}_{next_n:03d}"
    run_dir.mkdir(parents=True, exist_ok=True)
    return str(run_dir), next_n


class AverageMeter:
    """Track running average of a metric."""

    def __init__(self, name=""):
        self.name = name
        self.reset()

    def reset(self):
        self.val = 0.0
        self.avg = 0.0
        self.sum = 0.0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count


def save_checkpoint(state, is_best, checkpoint_dir, filename="checkpoint.pth"):
    """Save training checkpoint."""
    checkpoint_dir = Path(checkpoint_dir)
    checkpoint_dir.mkdir(parents=True, exist_ok=True)

    filepath = checkpoint_dir / filename
    torch.save(state, str(filepath))

    if is_best:
        best_path = checkpoint_dir / "model_best.pth"
        torch.save(state, str(best_path))
        print(f"  ★ Best model saved (val_loss={state['best_val_loss']:.6f})")


def load_checkpoint(filepath, model, optimizer=None, scheduler=None):
    """Load training checkpoint with backward compatibility."""
    checkpoint = torch.load(filepath, map_location="cpu")

    # Handle backward compatibility: old checkpoints stored corr_proj with
    # corr_dim = N * C_corr (concatenated samples), but new model uses
    # corr_dim = C_corr (per-sample). The only affected layer is
    # pose_update_net.corr_proj.0 (Conv2d).
    model_state = checkpoint["model_state_dict"]
    new_state = model.state_dict()

    mismatch_keys = []
    for k in model_state:
        if k in new_state and model_state[k].shape != new_state[k].shape:
            mismatch_keys.append((k, model_state[k].shape, new_state[k].shape))

    if mismatch_keys:
        print(f"  [Compatibility] Skipping {len(mismatch_keys)} mismatched layers:")
        for k, old_shape, new_shape in mismatch_keys:
            print(f"    {k}: checkpoint {old_shape} -> model {new_shape}")
        # Only load matching layers
        model_state = {k: v for k, v in model_state.items()
                       if k not in new_state or model_state[k].shape == new_state[k].shape}
        missing, unexpected = model.load_state_dict(model_state, strict=False)
        if missing:
            print(f"  [Compatibility] Uninitialized layers: {missing}")
    else:
        # Filter out unexpected keys (e.g., runtime caches like corr_block._cached_*)
        unexpected_keys = [k for k in model_state if k not in new_state]
        if unexpected_keys:
            print(f"  [Compatibility] Filtering {len(unexpected_keys)} unexpected keys:")
            for k in unexpected_keys:
                print(f"    {k}")
            model_state = {k: v for k, v in model_state.items() if k in new_state}
        model.load_state_dict(model_state)

    if optimizer is not None and "optimizer_state_dict" in checkpoint:
        optimizer.load_state_dict(checkpoint["optimizer_state_dict"])

    if scheduler is not None and "scheduler_state_dict" in checkpoint:
        scheduler.load_state_dict(checkpoint["scheduler_state_dict"])

    return checkpoint


