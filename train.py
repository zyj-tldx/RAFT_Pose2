def parse_args():
    parser = argparse.ArgumentParser(description="RAFT-Pose Training")

    # Data
    parser.add_argument("--config", type=str, required=True,
                        help="Path to JSON config file")
    parser.add_argument("--num_workers", type=int, default=4,
                        help="DataLoader workers")
    parser.add_argument("--image_size", type=int, nargs=2, default=None,
                        metavar=("H", "W"),
                        help="Override image size (H W). Recommended: 256 320")

    # Model architecture
    parser.add_argument("--image_encoder", type=str, default="basic",
                        choices=["basic", "small", "resnet18"])
    parser.add_argument("--shared_encoder", action="store_true",
                        help="Use same encoder for RGB and depth (Siamese). "
                             "Depth is repeated to 3 channels. No separate depth encoder.")
    parser.add_argument("--hidden_dim", type=int, default=128)
    parser.add_argument("--context_dim", type=int, default=64)
    parser.add_argument("--depth_dim", type=int, default=32)
    parser.add_argument("--corr_levels", type=int, default=4)
    parser.add_argument("--corr_radius", type=int, default=4)
    parser.add_argument("--num_iterations", type=int, default=6)
    parser.add_argument("--pose_sample_std", type=float, default=0.05)
    parser.add_argument("--init_pose_noise_std", type=float, default=0.00)

    # Training
    parser.add_argument("--epochs", type=int, default=50)
    parser.add_argument("--batch_size", type=int, default=4)
    parser.add_argument("--lr", type=float, default=1e-4)
    parser.add_argument("--weight_decay", type=float, default=1e-5)
    parser.add_argument("--grad_clip", type=float, default=1.0)
    parser.add_argument("--grad_accum_steps", type=int, default=1,
                        help="Gradient accumulation steps (effective batch = batch_size * grad_accum_steps)")
    parser.add_argument("--seq_loss_gamma", type=float, default=0.8,
                        help="Exponential weighting for sequence loss. "
                             "Later iterations weighted more (1.0 = equal, 0.8 = last ~2.4x first for K=4)")
    parser.add_argument("--scheduler", type=str, default="cosine",
                        choices=["cosine", "step", "none"])

    # Loss weights
    parser.add_argument("--rot_weight", type=float, default=100.0,
                        help="Weight for rotation loss (in degrees via rad-to-deg scaling)")
    parser.add_argument("--trans_weight", type=float, default=20.0,
                        help="Weight for translation loss (in meters)")

    # Curriculum learning
    parser.add_argument("--curriculum_start", type=float, default=0.1,
                        help="Initial noise level for curriculum learning (rad/m). "
                             "~0.01 = 0.57° / 1cm — model starts near GT.")
    parser.add_argument("--curriculum_end", type=float, default=0.5,
                        help="Final noise level for curriculum learning (rad/m). "
                             "~0.2 = 11.5° / 20cm — model handles large errors.")
    parser.add_argument("--curriculum_warmup", type=float, default=0.8,
                        help="Fraction of total epochs over which noise ramps up. "
                             "0.8 = noise reaches max at 80%% of training.")

    # Checkpointing
    parser.add_argument("--checkpoint_dir", type=str, default="checkpoints")
    parser.add_argument("--resume", type=str, default=None,
                        help="Path to checkpoint to resume from")

    # Logging
    parser.add_argument("--log_interval", type=int, default=10)
    parser.add_argument("--tensorboard", action="store_true",
                        help="Enable TensorBoard logging")
    parser.add_argument("--tb_dir", type=str, default="runs")

    # Device
    parser.add_argument("--device", type=str, default="cuda" if torch.cuda.is_available() else "cpu")
    parser.add_argument("--seed", type=int, default=42)

    # Memory optimization
    parser.add_argument("--use_amp", action="store_true",
                        help="Enable Automatic Mixed Precision (float16) for ~40%% memory reduction")
    parser.add_argument("--use_checkpoint", action="store_true",
                        help="Enable gradient checkpointing on encoders for ~50%% activation memory reduction")
    parser.add_argument("--coarse_to_fine", action="store_true",
                        help="Use coarse-to-fine correlation sampling: evaluate all N samples on "
                             "coarsest pyramid level first, then full multi-level sampling on top-K only. "
                             "Reduces peak memory from O(B*N) to O(B*K) for fine sampling.")
    parser.add_argument("--corr_temperature", type=float, default=1.0,
                        help="Correlation volume temperature. 1.0=raw cosine similarity. "
                             ">1.0=smoother landscape (recommended for ResNet18: 2.0-5.0). "
                             "Smooths sharp correlation peaks to prevent PoseUpdateNet overshooting.")
    parser.add_argument("--max_rot_step", type=float, default=0.3,
                        help="Maximum rotation step per iteration in radians. "
                             "Clamps predicted rot_vec to [-max_rot_step, max_rot_step]. "
                             "Prevents overshooting. Default: 0.3 rad (≈17°).")
    parser.add_argument("--max_trans_step", type=float, default=0.3,
                        help="Maximum translation step per iteration in meters. "
                             "Clamps predicted dt to [-max_trans_step, max_trans_step]. "
                             "Prevents overshooting. Default: 0.3 m.")
    parser.add_argument("--delta_loss_weight", type=float, default=1.0,
                        help="Weight for delta pose supervision loss. 0 to disable. "
                             "Directly supervises the predicted (rot_vec, dt) against the true delta. "
                             "Recommended: 1.0-10.0")
    parser.add_argument("--pretrained_encoder", type=str, default=None,
                        help="Path to pretrained encoder checkpoint (from pretrain_encoder.py). "
                             "Loads image_encoder, depth_encoder, depth_feat_align weights.")
    parser.add_argument("--freeze_encoder_epochs", type=int, default=0,
                        help="Freeze encoder for first N epochs during end-to-end training. "
                             "0 = no freezing. Recommended: 2-5 epochs after pretraining.")
    parser.add_argument("--resume_model_only", action="store_true",
                        help="Only load model weights from checkpoint, ignore optimizer/scheduler/epoch. "
                             "Useful when you want to change lr, epochs, or scheduler after resuming.")

    return parser.parse_args()


def main():
    args = parse_args()

    # Seed
    torch.manual_seed(args.seed)
    np.random.seed(args.seed)

    device = torch.device(args.device)
    print(f"Device: {device}")
    print(f"Config: {args.config}")

    # ─── Run directory ────────────────────────────────────────────────────
    run_dir, run_n = get_next_run_dir(args.checkpoint_dir, prefix="runs")
    log_path = Path(run_dir) / "train_log.txt"
    print(f"Run directory: {run_dir}")

    def log_print(msg, also_stdout=True):
        """Print to both console and log file."""
        line = msg + "\n"
        if also_stdout:
            print(msg, flush=True)
        with open(log_path, "a") as f:
            f.write(line)

    log_print(f"Device: {device}")
    log_print(f"Config: {args.config}")
    log_print(f"Run directory: {run_dir}")
    log_print(f"Arguments: {json.dumps(vars(args), indent=2)}")

    # ─── Data ─────────────────────────────────────────────────────────────
    train_loader = get_dataloader(
        args.config, split="train",
        batch_size=args.batch_size,
        num_workers=args.num_workers,
        image_size=tuple(args.image_size) if args.image_size else None,
        augment=False,
    )
    val_loader = get_dataloader(
        args.config, split="val",
        batch_size=args.batch_size,
        num_workers=args.num_workers,
        image_size=tuple(args.image_size) if args.image_size else None,
    )

    # ─── Model ────────────────────────────────────────────────────────────
    model = build_model(args).to(device)

    n_params = sum(p.numel() for p in model.parameters())
    n_trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"Model parameters: {n_params:,} (trainable: {n_trainable:,})")

    # ─── Loss ─────────────────────────────────────────────────────────────
    criterion = PoseLoss(rot_weight=args.rot_weight, trans_weight=args.trans_weight)
    delta_criterion = DeltaPoseLoss(rot_weight=args.rot_weight, trans_weight=args.trans_weight)
    delta_loss_weight = getattr(args, 'delta_loss_weight', 1.0)
    if delta_loss_weight > 0:
        log_print(f"Delta supervision loss enabled (weight={delta_loss_weight})")

    # ─── Optimizer & Scheduler ────────────────────────────────────────────
    optimizer = torch.optim.AdamW(
        model.parameters(),
        lr=args.lr,
        weight_decay=args.weight_decay,
    )

    if args.scheduler == "cosine":
        scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
            optimizer, T_max=args.epochs, eta_min=args.lr * 0.01
        )
    elif args.scheduler == "step":
        scheduler = torch.optim.lr_scheduler.StepLR(
            optimizer, step_size=args.epochs // 3, gamma=0.1
        )
    else:
        scheduler = None

    # ─── Pretrained Encoder ────────────────────────────────────────────────
    if getattr(args, 'pretrained_encoder', None):
        pretrain_path = args.pretrained_encoder
        if os.path.isfile(pretrain_path):
            log_print(f"Loading pretrained encoder from: {pretrain_path}")
            pretrain_ckpt = torch.load(pretrain_path, map_location="cpu")
            pretrain_state = pretrain_ckpt.get("model_state_dict", pretrain_ckpt)
            # Only load encoder-related weights
            encoder_state = {k: v for k, v in pretrain_state.items()
                             if any(k.startswith(p) for p in
                                    ['image_encoder', 'depth_encoder', 'depth_feat_align'])}
            missing, unexpected = model.load_state_dict(encoder_state, strict=False)
            log_print(f"  Loaded {len(encoder_state)} encoder params")
            if missing:
                non_encoder_missing = [k for k in missing
                                       if not any(k.startswith(p) for p in
                                                  ['image_encoder', 'depth_encoder', 'depth_feat_align'])]
                if non_encoder_missing:
                    log_print(f"  Non-encoder missing (expected): {len(non_encoder_missing)} layers")
            if unexpected:
                log_print(f"  Unexpected keys: {unexpected}")
            log_print(f"  ★ Pretrained encoder loaded successfully")
        else:
            log_print(f"Warning: pretrained encoder not found at {pretrain_path}")

    # ─── Resume ───────────────────────────────────────────────────────────
    start_epoch = 1
    best_val_loss = float("inf")

    if args.resume:
        if os.path.isfile(args.resume):
            if getattr(args, 'resume_model_only', False):
                # Only load model weights, start fresh with optimizer/scheduler/epoch
                print(f"Loading model weights from: {args.resume} (model only, no optimizer/scheduler)")
                ckpt = load_checkpoint(args.resume, model, optimizer=None, scheduler=None)
                log_print(f"  Model weights loaded (model-only mode, starting from epoch 1)")
            else:
                print(f"Resuming from checkpoint: {args.resume}")
                ckpt = load_checkpoint(args.resume, model, optimizer, scheduler)
                start_epoch = ckpt.get("epoch", 0) + 1
                best_val_loss = ckpt.get("best_val_loss", float("inf"))
                print(f"  Resumed at epoch {start_epoch}, best_val_loss={best_val_loss:.6f}")
        else:
            print(f"Warning: checkpoint not found at {args.resume}")

    # ─── TensorBoard ─────────────────────────────────────────────────────
    writer = None
    if args.tensorboard:
        tb_log_dir = Path(run_dir) / "tensorboard"
        writer = SummaryWriter(log_dir=str(tb_log_dir))
        log_print(f"TensorBoard logging to: {tb_log_dir}")

    # ─── Training Loop ────────────────────────────────────────────────────
    if getattr(args, 'use_amp', False):
        log_print("AMP (float16 encoders) enabled")
    if getattr(args, 'use_checkpoint', False):
        log_print("Gradient checkpointing enabled")
    log_print(f"\n{'='*60}")
    log_print(f"Training: {args.epochs} epochs, batch_size={args.batch_size}, lr={args.lr}")
    log_print(f"{'='*60}\n")

    freeze_encoder_epochs = getattr(args, 'freeze_encoder_epochs', 0)

    for epoch in range(start_epoch, args.epochs + 1):
        # Freeze encoder for first N epochs if requested
        if freeze_encoder_epochs > 0:
            for name, param in model.named_parameters():
                if any(k in name for k in ['image_encoder', 'depth_encoder', 'depth_feat_align']):
                    param.requires_grad = (epoch > freeze_encoder_epochs)
            if epoch <= freeze_encoder_epochs:
                log_print(f"  [Epoch {epoch}] Encoder FROZEN (unfreeze after epoch {freeze_encoder_epochs})")
            elif epoch == freeze_encoder_epochs + 1:
                log_print(f"  [Epoch {epoch}] Encoder UNFROZEN — now training end-to-end")

        # Train
        train_metrics = train_one_epoch(
            model, train_loader, criterion, optimizer, device, epoch,
            grad_clip=args.grad_clip, log_interval=args.log_interval,
            grad_accum_steps=args.grad_accum_steps,
            seq_loss_gamma=args.seq_loss_gamma,
            total_epochs=args.epochs,
            curriculum_start=args.curriculum_start,
            curriculum_end=args.curriculum_end,
            curriculum_warmup=args.curriculum_warmup,
            delta_criterion=delta_criterion,
            delta_loss_weight=delta_loss_weight,
        )

        # Validate
        val_metrics = validate(model, val_loader, criterion, device)

        # Scheduler step
        if scheduler is not None:
            scheduler.step()

        # Logging
        # Compute current curriculum noise level for display
        cur_progress = min(1.0, (epoch - 1) / max(1, args.epochs * args.curriculum_warmup))
        cur_noise = args.curriculum_start + (args.curriculum_end - args.curriculum_start) * cur_progress
        log_print(
            f"Epoch [{epoch}/{args.epochs}] (lr={optimizer.param_groups[0]['lr']:.2e}, noise={cur_noise:.3f})"
        )
        log_print(
            f"  Train → Loss: {train_metrics['train_loss']:.4f}  "
            f"Rot: {train_metrics['train_rot_deg']:.2f}°  "
            f"Trans: {train_metrics['train_trans_m']:.4f}m"
        )
        log_print(
            f"  Val   → Loss: {val_metrics['val_loss']:.4f}  "
            f"Rot: {val_metrics['val_rot_deg']:.2f}°  "
            f"Trans: {val_metrics['val_trans_m']:.4f}m"
        )

        # TensorBoard
        if writer is not None:
            for k, v in train_metrics.items():
                writer.add_scalar(k, v, epoch)
            for k, v in val_metrics.items():
                writer.add_scalar(k, v, epoch)
            writer.add_scalar("lr", optimizer.param_groups[0]["lr"], epoch)

        # Checkpoint
        is_best = val_metrics["val_loss"] < best_val_loss
        best_val_loss = min(val_metrics["val_loss"], best_val_loss)

        save_checkpoint(
            {
                "epoch": epoch,
                "model_state_dict": model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "scheduler_state_dict": scheduler.state_dict() if scheduler else None,
                "best_val_loss": best_val_loss,
                "train_metrics": train_metrics,
                "val_metrics": val_metrics,
                "args": vars(args),
            },
            is_best=is_best,
            checkpoint_dir=run_dir,
        )

        log_print("")

    # ─── Cleanup ──────────────────────────────────────────────────────────
    if writer is not None:
        writer.close()

    log_print(f"Training complete. Best val_loss: {best_val_loss:.6f}")
    log_print(f"Checkpoints saved to: {run_dir}")


if __name__ == "__main__":
    main()
